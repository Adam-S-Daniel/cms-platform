#!/usr/bin/env node
// Content PR conformance guard — pure logic (no network, no octokit, no fs).
//
// WHY: Decap's editorial workflow is a contract, not just a UI convenience —
// every content change is ONE entry on a `cms/<collection>/<slug>` branch,
// opened by Decap itself, carrying a `decap-cms/<status>` label that drives
// the draft → review → ready → publish pipeline (cms-editorial-workflow.yml),
// a stable per-entry preview alias, and publish/schedule semantics. A PR that
// edits the same content paths WITHOUT going through /admin skips all of
// that: there's no draft/review status, no preview alias, and (for posts) no
// guarantee `published` / `publish_date` mean what the CMS expects. Worse, it
// can race Decap's own on-repo state (labels, branch naming, in-flight
// entries) since Decap doesn't know the edit happened.
//
// This module decides, for a single PR, whether its diff touches CMS-managed
// content and — if so — whether the PR is Decap-shaped (or has been granted
// an explicit override). The caller (cms-editorial-workflow.yml's
// validate-content job) wires this to the GitHub API: listing changed files,
// reading the site's seam YAML + `_config.yml` off the checked-out tree, and
// posting/updating a PR comment with the verdict. Keeping all of that out of
// this module means the decision logic is testable with plain objects and
// strings (see e2e/content-pr-guard.test.js) and has nothing to mock.
//
// Usage (from a github-script step):
//   const { evaluateContentGuard } = require('.cms-platform/scripts/content-pr-guard.js');
//   const result = evaluateContentGuard({ pr, changedFiles, seamYamlText, decapBranchPrefix, adminUrl });
"use strict";

// Marks the PR comment this guard owns so re-runs update it in place instead
// of piling up duplicate comments.
const COMMENT_MARKER = "<!-- content-pr-guard -->";

// Applied by a maintainer to let a deliberate, non-Decap content edit through
// (fixture repair, bulk migration, …). Applying the label fires a `labeled`
// event, which re-runs this check — and it then passes.
const OVERRIDE_LABEL = "content-guard/override";

// The literal marker Decap CMS puts at the start of every editorial-workflow
// PR body it opens. Mirrors the same string in label-non-decap-prs.yml's
// isDecap triad — keep both in sync if Decap ever changes this text.
const DECAP_BODY_MARKER = "Automatically generated by Decap CMS";

// Base CMS-managed content directories, derived from theme/admin/config.base.yml's
// base collections' `folder:` values (_posts, _tags, _projects, pages, _e2e)
// plus its `media_folder` (assets/images/uploads). Update BOTH together if
// either changes.
const BASE_CONTENT_DIRS = [
  "_posts/",
  "_tags/",
  "_projects/",
  "pages/",
  "_e2e/",
  "assets/images/uploads/",
];

function normalizeDir(raw) {
  const trimmed = String(raw).trim().replace(/^["']|["']$/g, "").trim();
  if (!trimmed) return "";
  return trimmed.endsWith("/") ? trimmed : `${trimmed}/`;
}

// Extract every site-specific collection folder from a consumer's
// admin/collections.site.yml (the seam the base config injects at
// `__SITE_COLLECTIONS__`). A line-regex is fine here — this is leaf-token
// extraction over a simple list-of-collections YAML, not general parsing.
function seamContentDirs(seamYamlText) {
  if (!seamYamlText) return [];
  const dirs = [];
  const re = /^\s*folder:\s*(.+?)\s*$/gm;
  let match;
  while ((match = re.exec(seamYamlText)) !== null) {
    const dir = normalizeDir(match[1]);
    if (dir) dirs.push(dir);
  }
  return dirs;
}

function labelNames(labels) {
  return (labels || []).map((l) => (typeof l === "string" ? l : (l && l.name) || ""));
}

// Mirrors the isDecap triad in .github/workflows/label-non-decap-prs.yml: a
// PR is Decap-shaped if its head branch uses the Decap branch convention, its
// body carries Decap's marker, or it already carries a decap-cms/* status
// label. Any one signal is enough — a genuine Decap PR should never be
// mis-flagged even if one signal is missing or spoofed.
function isDecapShaped(pr, decapBranchPrefix) {
  if ((pr && pr.head && pr.head.ref || "").startsWith(decapBranchPrefix)) return true;
  if ((pr && pr.body || "").startsWith(DECAP_BODY_MARKER)) return true;
  return labelNames(pr && pr.labels).some((name) => name.startsWith("decap-cms/"));
}

const FILE_LIST_CAP = 20;

function buildCommentBody(contentFiles, adminUrl) {
  const shown = contentFiles.slice(0, FILE_LIST_CAP);
  const remaining = contentFiles.length - shown.length;
  const fileList =
    shown.map((f) => `- \`${f}\``).join("\n") + (remaining > 0 ? `\n- _+${remaining} more_` : "");

  return [
    COMMENT_MARKER,
    "",
    "### This PR can't merge as-is: it edits CMS-managed content outside Decap",
    "",
    "This PR touches the following file(s), which this site manages through Decap CMS's editorial workflow — but the PR wasn't created by Decap:",
    "",
    fileList,
    "",
    "**Why this is blocked.** Every content change on this site is supposed to be one entry on a `cms/<collection>/<slug>` branch, opened by Decap itself. Editing these paths any other way skips everything Decap's editorial workflow depends on: the entry's draft/review/ready status, its stable per-entry preview alias, publish/schedule semantics (`published` / `publish_date`), and consistent media handling. A change that lands outside that flow can also collide with Decap's own on-repo state (labels, branch naming, in-flight entries), since Decap has no idea the edit happened.",
    "",
    `**What to do instead.** Make this change at [${adminUrl}](${adminUrl}) — the Decap Workflow tab creates a PR in exactly this shape (\`cms/<collection>/<slug>\` branch, entry body, status labels) automatically.`,
    "",
    `**Escape hatch.** If this genuinely is deliberate maintenance — repairing a broken fixture, a bulk migration — rather than an ordinary content edit, a maintainer can apply the \`${OVERRIDE_LABEL}\` label to this PR. That fires the \`labeled\` event, which re-runs this check, and it will then pass.`,
  ].join("\n");
}

/**
 * Evaluate whether a PR's content changes conform to the Decap editorial
 * workflow.
 *
 * @param {object} params
 * @param {object} params.pr - the pull_request payload object.
 * @param {string[]} params.changedFiles - every file path touched by the PR.
 * @param {string|null} params.seamYamlText - raw admin/collections.site.yml
 *   text, or null/empty if the site has no site-specific collections.
 * @param {string} params.decapBranchPrefix - e.g. "cms/".
 * @param {string[]} [params.extraContentDirs] - extra dirs to treat as content.
 * @param {string} params.adminUrl - the site's /admin/ URL, for the comment.
 * @returns {{verdict: "pass"|"fail", reason: string, contentFiles: string[], commentBody: string|null}}
 */
function evaluateContentGuard({
  pr,
  changedFiles,
  seamYamlText,
  decapBranchPrefix,
  extraContentDirs = [],
  adminUrl,
}) {
  const contentDirs = [...BASE_CONTENT_DIRS, ...seamContentDirs(seamYamlText), ...extraContentDirs];
  const contentFiles = (changedFiles || []).filter((f) => contentDirs.some((d) => f.startsWith(d)));

  if (contentFiles.length === 0) {
    return { verdict: "pass", reason: "no-content-changes", contentFiles, commentBody: null };
  }
  if (isDecapShaped(pr, decapBranchPrefix)) {
    return { verdict: "pass", reason: "decap-shaped", contentFiles, commentBody: null };
  }
  if (labelNames(pr && pr.labels).some((name) => name === OVERRIDE_LABEL)) {
    return { verdict: "pass", reason: "override", contentFiles, commentBody: null };
  }

  return {
    verdict: "fail",
    reason: "non-decap-content-change",
    contentFiles,
    commentBody: buildCommentBody(contentFiles, adminUrl),
  };
}

module.exports = {
  COMMENT_MARKER,
  OVERRIDE_LABEL,
  DECAP_BODY_MARKER,
  BASE_CONTENT_DIRS,
  seamContentDirs,
  isDecapShaped,
  evaluateContentGuard,
};
