// @lane: local — exercises the locally-served Atom/RSS feeds + per-post share row
const path = require("node:path");
const { test, expect } = require("./base");
const { discoverTags, discoverPost } = require("./content-fixtures");
const cap = require("./site-capabilities");

// SITE_ROOT for the #33 capability gate (build-INDEPENDENT, so it's correct in
// the preview/prod @parity lanes too). Unset → the harness's parent.
const SITE_ROOT = process.env.SITE_ROOT || path.resolve(__dirname, "..");

// Acceptance for the RSS/Atom feeds + per-post share row.
//
// The Atom feed for *all posts* is generated by jekyll-feed; the per-tag
// feeds are emitted by `_plugins/tag_feeds.rb` via `_layouts/atom_feed.xml`.
// Both flavors must share the same shape so any reader that subscribes
// to one parses the other.
//
// The share row lives on every post page; it is intentionally subtle —
// these tests verify presence and intent URLs, not pixels.
//
// Post and tag fixtures are discovered at runtime via
// e2e/content-fixtures.js rather than hardcoded — when the site has no
// posts or no tags, the dependent tests self-skip with a clear reason
// rather than failing because a specific fixture name was removed.

const ATOM_NS = 'xmlns="http://www.w3.org/2005/Atom"';

async function fetchText(page, url) {
  const response = await page.request.get(url);
  expect(response.status(), `${url} should respond 200`).toBe(200);
  return { response, body: await response.text() };
}

test.describe("Atom feeds", () => {
  // #33 — a single-page consumer that opts out of the posts collection via
  // cms.base_collections (v0.1.7) has no blog, so the all-posts Atom feed
  // contract doesn't apply (jekyll-feed still emits a `/feed.xml`, but it's an
  // empty, postless feed — and its title is the SITE author, not necessarily
  // this spec's expected one). Skip the feed-shape/metadata test precisely
  // when posts is opted out; the sibling per-tag / feed-content / share-row
  // tests already self-skip via discoverTags/discoverPost. The full
  // fixture-site + adamdaniel.ai keep posts → this runs unchanged there.
  test("/feed.xml exists, is Atom, and contains the right metadata", async ({ page }) => {
    test.skip(
      !cap.keepsBaseCollection(SITE_ROOT, "posts"),
      "consumer opts out of the posts collection via cms.base_collections — no blog Atom feed contract to assert (#33)",
    );
    const { response, body } = await fetchText(page, "/feed.xml");
    const ct = (response.headers()["content-type"] || "").toLowerCase();
    expect(ct).toMatch(/xml/);
    expect(body).toContain(ATOM_NS);
    expect(body).toMatch(/<feed\b/);
    expect(body).toMatch(
      /<link[^>]+(rel="self"[^>]+href="[^"]*feed\.xml|href="[^"]*feed\.xml"[^>]+rel="self")/,
    );
    expect(body).toMatch(/<title[^>]*>.*Adam Daniel.*<\/title>/);
  });

  test("each discovered tag emits a valid per-tag Atom feed at /tags/<slug>/feed.xml", async ({
    page,
  }) => {
    const tags = await discoverTags(page);
    test.skip(tags.length === 0, "no tags exist on the site — no per-tag feeds to assert against");
    for (const { slug } of tags) {
      const { body } = await fetchText(page, `/tags/${slug}/feed.xml`);
      expect(body, `/tags/${slug}/feed.xml lacks Atom namespace`).toContain(ATOM_NS);
      expect(body).toMatch(/<feed\b/);
      expect(body).toMatch(
        new RegExp(`<link[^>]+href="[^"]*/tags/${slug}/feed\\.xml"[^>]+rel="self"`),
      );
      // Subtitle scopes the feed to the tag.
      expect(body).toMatch(/Posts tagged/);
    }
  });

  test("/feed.xml content includes every published post", async ({ page }) => {
    // Discover what posts ARE published (read /blog/), then confirm
    // each one shows up in the Atom feed. This catches the regression
    // where jekyll-feed silently skips a post because it has
    // `feed_exclude: true` set, or where a layout change drops the
    // post's frontmatter from the feed input. Structure-only tests
    // above pass even when the feed is empty.
    const post = await discoverPost(page);
    test.skip(!post, "no published posts to verify against — skipping feed-content check");
    const { body } = await fetchText(page, "/feed.xml");
    // Each post's permalink ends with /blog/<slug>/. The feed's
    // <entry><link href="…"/> should reference the canonical URL.
    expect(body, `feed.xml should reference ${post.url} (the discovered post)`).toMatch(
      new RegExp(`href="[^"]*${post.url.replace(/\//g, "\\/")}"`),
    );
    // The post's title should appear in an <entry><title>…</title>.
    // Escape any regex special chars in the title.
    const escapedTitle = post.title.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
    expect(body, `feed.xml should contain the post title "${post.title}"`).toMatch(
      new RegExp(`<title[^>]*>[^<]*${escapedTitle}`),
    );
  });

  test("per-tag feed isolation: contains only posts with that tag", async ({ page }) => {
    // The crucial contract for /tags/<slug>/feed.xml: it MUST contain
    // only the posts tagged with <slug>, NOT every post. If the
    // _plugins/tag_feeds.rb plugin's filter regresses, a tag's feed
    // could leak unrelated posts. This test fetches each tag's feed
    // and the global /feed.xml, then asserts that any post URL in the
    // tag's feed is also in the global feed (sanity), AND that the
    // tag's feed has at most as many <entry> elements as the tag
    // says it has (count discovered from /tags/).
    const tags = await discoverTags(page);
    test.skip(tags.length === 0, "no tags exist — no per-tag feeds to isolate-check");
    const { body: globalBody } = await fetchText(page, "/feed.xml");
    for (const { slug, count } of tags) {
      const { body } = await fetchText(page, `/tags/${slug}/feed.xml`);
      const entryCount = (body.match(/<entry\b/g) || []).length;
      expect(
        entryCount,
        `/tags/${slug}/feed.xml has ${entryCount} entries; the tag claims ${count} on /tags/`,
      ).toBeLessThanOrEqual(count);
      // Every <entry><link href="…"> in the per-tag feed must also
      // appear in the global /feed.xml. (Reverse direction is not
      // asserted — global feed contains posts from ALL tags.)
      const tagEntryHrefs = Array.from(
        body.matchAll(/<entry\b[\s\S]*?<link[^>]*href="([^"]+)"/g),
      ).map((m) => m[1]);
      for (const href of tagEntryHrefs) {
        expect(globalBody, `tag-feed href ${href} should also be in /feed.xml`).toContain(href);
      }
    }
  });
});

test.describe("Feed-link icons surface the feeds in the UI", () => {
  test('homepage "Latest Posts" header links to /feed.xml', async ({ page }) => {
    // The homepage .feed-link icon is rendered alongside the "Latest
    // Posts" header, which only appears when at least one post is
    // published. Skip when the site has no posts (the feed itself
    // still works — that's covered by the /feed.xml Atom test
    // above).
    const post = await discoverPost(page);
    test.skip(
      !post,
      "no published posts → homepage 'Latest Posts' section + feed-link don't render",
    );

    await page.goto("/");
    const link = page.locator('.feed-link[href$="/feed.xml"]').first();
    await expect(link).toBeVisible();
    await expect(link).toHaveAttribute("aria-label", /Atom feed/i);
    // Icon is rendered, not a text label.
    await expect(link.locator("svg")).toBeVisible();
  });

  test("/blog/ header links to /feed.xml", async ({ page }) => {
    // Same reasoning — /blog/'s page-header is gated on having posts
    // to list. Without posts, /blog/ may render an empty-state body
    // and no feed-link.
    const post = await discoverPost(page);
    test.skip(!post, "no published posts → /blog/ page-header + feed-link don't render");

    await page.goto("/blog/");
    const link = page.locator(".page-header .feed-link").first();
    await expect(link).toBeVisible();
    await expect(link).toHaveAttribute("href", /\/feed\.xml$/);
  });

  test("/tags/<slug>/ header links to its per-tag feed and advertises it in <head>", async ({
    page,
  }) => {
    const tags = await discoverTags(page);
    test.skip(tags.length === 0, "no tags on the site — no per-tag header to assert");
    const slug = tags[0].slug;
    await page.goto(`/tags/${slug}/`);

    const link = page.locator(".page-header .feed-link").first();
    await expect(link).toBeVisible();
    await expect(link).toHaveAttribute("href", new RegExp(`/tags/${slug}/feed\\.xml$`));

    // Browser-discoverable feed in <head>.
    const alt = page.locator(
      `head link[rel="alternate"][type="application/atom+xml"][href$="/tags/${slug}/feed.xml"]`,
    );
    await expect(alt).toHaveCount(1);
  });
});

test.describe("Feed-link icon survives CSS load failure (webkit)", () => {
  // SVG without CSS reverts to its default replaced-element size — Firefox
  // and WebKit pick 300×150 if no width/height attributes survive on the
  // element. Without `assets/css/main.css`, the RSS icon shouldn't blow
  // up to that default. We exercise this on webkit projects because it's
  // the engine that surfaced the regression originally.
  test("RSS icon stays icon-sized when main.css fails to load", async ({ page, browserName }) => {
    test.skip(browserName !== "webkit", "webkit-only regression case");
    // Same homepage gating as above — the .feed-link only renders
    // when the "Latest Posts" section has at least one post.
    const post = await discoverPost(page);
    test.skip(
      !post,
      "no published posts → homepage feed-link doesn't render, no icon to size-check",
    );

    // Abort every main.css request so the icon renders unstyled.
    await page.route("**/main.css*", (route) => route.abort());

    await page.goto("/");
    const link = page.locator('.feed-link[href$="/feed.xml"]').first();
    const svg = link.locator("svg");
    await svg.waitFor({ state: "attached" });
    const box = await svg.boundingBox();
    expect(box, "svg must have a bounding box").not.toBeNull();
    // 16px is the conventional inline-icon size; allow up to that. A bare
    // SVG without width/height styling explodes to 300×150 — way over.
    expect(box.width).toBeLessThanOrEqual(16);
  });
});

test.describe("Share row on a post", () => {
  // Post fixture is whatever post the site lists first on /blog/. The
  // share-row contract is the same for every post — the spec doesn't
  // need to anchor on a specific one. When no post is published (e.g.
  // _posts/ is empty or only contains a future-dated canary), every
  // test in this group skips with a clear reason.

  test("renders an understated share row at the bottom of the post", async ({ page }) => {
    const post = await discoverPost(page);
    test.skip(!post, "no published posts on the site — share row has no host page");
    await page.goto(post.url);

    const row = page.locator(".share-row");
    await expect(row).toBeVisible();
    await expect(row).toHaveAttribute("aria-label", /share/i);

    // Five icon links.
    const links = row.locator(".share-link");
    await expect(links).toHaveCount(5);

    // No visible text labels other than the small "Share" tag.
    await expect(row.locator(".share-row-label")).toHaveText(/share/i);
  });

  test("LinkedIn / X / Bluesky intents carry the post URL and title", async ({ page }) => {
    const post = await discoverPost(page);
    test.skip(!post, "no published posts on the site");
    await page.goto(post.url);

    const row = page.locator(".share-row");
    const linkedin = row.locator('a[href*="linkedin.com/sharing"]');
    const x = row.locator('a[href*="twitter.com/intent/tweet"]');
    const bluesky = row.locator('a[href*="bsky.app/intent/compose"]');

    await expect(linkedin).toHaveCount(1);
    await expect(x).toHaveCount(1);
    await expect(bluesky).toHaveCount(1);

    // The slug is the path segment under /blog/. Every share-intent
    // URL must include it (so re-shares land on the right post).
    expect(await linkedin.getAttribute("href")).toContain(post.slug);
    const xHref = await x.getAttribute("href");
    expect(xHref).toContain(post.slug);
    const bskyHref = await bluesky.getAttribute("href");
    expect(bskyHref).toContain(post.slug);

    // The title must be carried (form- or percent-encoded) on every
    // intent that takes a quoted body. Build a regex from the first
    // non-empty word of the title — covers both encoding styles
    // without depending on a specific fixture's exact phrasing.
    const firstWord = post.title.split(/\s+/).find((w) => w.length > 0);
    if (firstWord) {
      const wordRe = new RegExp(firstWord.replace(/[.*+?^${}()|[\]\\]/g, "\\$&"), "i");
      expect(xHref, "X intent URL is missing the post title").toMatch(wordRe);
      expect(bskyHref, "Bluesky intent URL is missing the post title").toMatch(wordRe);
    }
  });

  test("Mastodon and Copy buttons are buttons (not links) and carry data attrs", async ({
    page,
  }) => {
    const post = await discoverPost(page);
    test.skip(!post, "no published posts on the site");
    await page.goto(post.url);

    const masto = page.locator(".share-mastodon");
    const copy = page.locator(".share-copy");

    await expect(masto).toBeVisible();
    await expect(copy).toBeVisible();
    expect(await masto.evaluate((el) => el.tagName)).toBe("BUTTON");
    expect(await copy.evaluate((el) => el.tagName)).toBe("BUTTON");

    const slugRe = new RegExp(`/blog/${post.slug}/?$`);
    expect(await copy.getAttribute("data-share-copy-url")).toMatch(slugRe);
    expect(await masto.getAttribute("data-share-mastodon-url")).toMatch(slugRe);
  });

  test("Copy button writes the post URL to the clipboard", async ({
    page,
    browserName,
    context,
  }, testInfo) => {
    test.skip(
      browserName !== "chromium",
      "clipboard permission API is only reliably grantable on chromium",
    );
    test.skip(
      testInfo.project.use.forcedColors === "active",
      "skipping under forced-colors — not relevant to the JS behavior",
    );
    const post = await discoverPost(page);
    test.skip(!post, "no published posts on the site");

    await context.grantPermissions(["clipboard-read", "clipboard-write"]);

    await page.goto(post.url);
    const copy = page.locator(".share-copy");
    await copy.click();

    await expect(copy).toHaveClass(/share-copied/);

    const clipboardText = await page.evaluate(() => navigator.clipboard.readText());
    expect(clipboardText).toMatch(new RegExp(`/blog/${post.slug}/?$`));
  });
});
